						Outra caracter&iacute;stica muito importante do g&aacute;s de cozinha &eacute; que ele &eacute; mais pesado do que o ar, tendendo, nos casos de vazamentos, a depositar-se em locais mais baixos, ou como uma camada sobre o piso da &aacute;rea onde se encontra o botij&atilde;o. Isto torna dif&iacute;cil a sua dispers&atilde;o, particularmente se n&atilde;o existirem correntes de ar (vento) que o transporte para fora do ambiente. Desta forma, qualquer microvazamento ser&aacute; facilmente percebido e n&oacute;s poderemos agir de forma prudente.<br />
